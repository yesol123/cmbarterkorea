          ③"회사"는 약정에 따라 일반회원에게 귀속된 저작권을 사용하는 경우 당해 일반회원에게 통보하여야 합니다.<br><br>

          제20조 (일반회원의 물물교환중개서비스 이용)<br>
          ①"회사"는  물물교환중개서비스와 관련하여 물물교환중개자로서 일반회원과 ＂가맹점회원", 또는 일반회원 간의 자유로운 상품 등의 거래를 위한 시스템을 운영 및 관리, 제공할 뿐이므로 일반회원은 CM으로  재화 또는 용역을 구매하기 전에 반드시 재화 또는 용역의 상세 내용과 거래조건을 정확하게 확인한 후 구매를 신청하여야 합니다.<br>
