          ⑤ 일반회원은 ＂가맹점회원＂과의 상거래 절차에서 분쟁이 발생하면 분쟁의 해결을 위하여 성실히 임해야 하며, 불성실하게 분쟁해결 절차에 임함으로써 ＂가맹점회원＂ 및＂회사＂에 손실과 손해가 발생하면 그에 대한 모든 책임을 부담하여야 합니다.<br>
